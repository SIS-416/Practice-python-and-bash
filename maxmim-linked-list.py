word = input("Enter a word: ")

length = len(word)
print(f"Length of the word is :{length}")

revers = word[::-1]
print(f"Reverse of the word is :{revers}")

upper = word.upper()
print(f"Upper case of the word is :{upper}")
lower = word.lower()
print(f"Lower case of the word is :{lower}")
